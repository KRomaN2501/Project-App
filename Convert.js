class Convert {

    /**
     * @param {string} str
     * @returns {number}
     */
    static convertToNumber(str) { //ЗАГЛУШКА
        return Number(str);
    }

    /**
     * @param {string} str
     * @returns {boolean}
     */
    static convertToBool(str) { //ЗАГЛУШКА
        return str === "true" ? true : false;
    }
}